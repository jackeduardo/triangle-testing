package determine_triangle;

public class Point {
    private double x;
    private double y;

    Point(double X, double Y) {
        x = X;
        y = Y;
    }

    public void setX(double X) {
        x = X;
    }

    public void setY(double Y) {
        y = Y;
    }

    public double getX(double X) {
        return x;
    }

    public double getY(double Y) {
        return Y;
    }


}
